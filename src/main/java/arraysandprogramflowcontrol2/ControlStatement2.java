package arraysandprogramflowcontrol2;

public class ControlStatement2 {

    public static void main(String[] args) {
        int control=0;

        switch (control){
            case 0:
                System.out.println("value 0");
                break;
            case 1:
                System.out.println("value 1");
                break;
            default:
                System.out.println("default");
        }
    }
}
